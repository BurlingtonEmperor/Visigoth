const menuOptions = document.getElementById("menu-options");

function openMenu () {
  menuOptions.style.display = "block";
  battleWindow.style.display = "block";

  characterRoster.style.display = "none";
}

function closeMenu () {
  menuOptions.style.display = "block";
  battleWindow.style.display = "block";

  characterRoster.style.display = "none";
}